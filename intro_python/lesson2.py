# lesson 3 - Arithmetic Operators

a = 10
b = 3

# addition operater +
print ("a: %d, b: %d, a + b: %d" %  (a, b, a + b))

# subtraction operator -
print ("a: %d, b: %d, a - b: %d" % (a, b, a - b))

# multiplication operator *
print ("a: %d, b: %d, a * b: %d" % (a, b, a * b))

# division operator /
print ("a: %d, b: %d, a / b: %.2f" %  (a, b, a/b))

# modulus operator
print ("a: %d, b: %d, a %% b: %d" % (a, b, a % b))

# exponentiation operator **
print ("a: %d, b: %d, a ** b: %d" % (a, b, a ** b))
